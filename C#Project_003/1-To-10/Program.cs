//For Loop

int num1 = 10;

for (int i = 0; i < num1; i++)
{
    Console.WriteLine($"Number: {i + 1}");
}

//While Loop

int num2 = 0;

while (num2 < 10)
{
    Console.WriteLine($"Number: {num2 + 1}");
    num2++;
}

//Do-While Loop

int num3 = 0;

do
{
    Console.WriteLine($"Number: {num3 + 1}");
    num3++;
}while(num3 < 10);